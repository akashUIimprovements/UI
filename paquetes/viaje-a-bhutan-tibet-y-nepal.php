<p>El almuerzo se servirá en el Restaurante Cafetería, situado a la mitad de la montaña. Después de haber explorado el monasterio, caminamos hasta el fondo del valle. Esta tarde, conduciremos a Thimphu, y pasaremos la noche.</p>
