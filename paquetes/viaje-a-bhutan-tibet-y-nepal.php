<p>A nuestra llegada al aeropuerto de Gonggar en Lhasa, un representante del glaciar del Himalaya nos recogerá del aeropuerto durante aproximadamente una hora en coche hasta nuestro hotel designado en Lhasa. Se recomienda tomar un montón de descanso para el resto del día con el fin de minimizar el mal de altura. Noche en Lhasa.</p>
