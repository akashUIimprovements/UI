<p>El Templo de Pashupatinath es uno de los lugares de peregrinación más sagrados para los hindúes de todo el mundo. El santuario budista de Boudhanath es una de las estupas más grandes del mundo, y uno de los destinos turísticos y de peregrinación budista más sagrados de Katmandú. Noche en Katmandú. </p>
<p>Comidas incluidas: Desayuno</p>
