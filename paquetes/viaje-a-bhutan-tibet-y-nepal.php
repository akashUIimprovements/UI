<p>El monasterio de Sera es un monasterio conservado de paredes encaladas y techos dorados. El templo de Jokhang es otro sitio sagrado importante que desentraña misterios más profundos del budismo tibetano. Además, paseamos por el bazar Barkhor (mercado). Con sus puestos al aire libre, tiendas y multitud de peregrinos, es el centro neurálgico de Lhasa. Noche en Lhasa.</p>
