<p>Luego pasamos por los campos de arroz a lo largo de las orillas del río y llegamos a Punakha. Después del almuerzo, visitamos Punakha Dzong. Construido entre 1637-1638, es el segundo dzong más antiguo y el segundo más grande de Bután. Noche en Punakha.</p>
