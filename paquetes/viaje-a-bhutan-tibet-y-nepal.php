                <p>Deje el paisaje del sur de Nepal para Pokhara, la hermosa ciudad del lago. Al entrar en Pokhara, somos recompensados con magníficas vistas del Himalaya incluyendo Dhaulagiri, Manaslu, Machhapuchhre, los cinco picos del Annapurna y otros. Hoy visitamos Davis Falls y Shiva Cave.</p>
<p>Davis Falls es una cascada única que fluye directamente en un canal estrecho y profundo que no tiene fin. La Cueva Shiva se encuentra cerca de Davis Fall y alberga un santuario dedicado al Señor Shiva, un dios hindú primario. Noche en Pokhara.</p>
