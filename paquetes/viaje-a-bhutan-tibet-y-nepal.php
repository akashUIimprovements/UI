<p>Mientras hacemos kayaking, podemos echar un vistazo de cerca a los cocodrilos Gharial y Mugger junto con delfines de agua dulce, nutrias y variedades de aves acuáticas. O, incluso podemos ir a la observación de aves o paseos por la naturaleza y acercarnos a una variedad de flora y fauna silvestres que se encuentran en el parque. Noche en Chitwan.</p>
