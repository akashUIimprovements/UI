<p>Durante el safari, nos adentramos en la selva y velamos ciervos, el rinoceronte de un cuerno en peligro de extinción, junto con otros animales salvajes. Si tiene suerte, incluso podemos ver al estrado Tigre Real de Bengala. Durante los paseos por la naturaleza disfrutamos de la compañía de una amplia variedad de flora y fauna y pasamos algún tiempo observando aves. Por la noche, habrá una presentación sobre la amplia gama de flora y fauna de CNP. Noche en Chitwan.</p>
<p>Comidas incluidas: Desayuno / Almuerzo / Cena</p>
