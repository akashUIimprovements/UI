<p>También mantiene un traje de armadura decorado con gemas, que fue usado por el emperador Qianlong y fue otorgado al monasterio como una ofrenda a Tsong Khapa, fundador de la secta Gelug. Durante la tarde, nos dirigimos a la cueva de Drayerpa, situada en un hermoso valle con un paisaje impresionante. Hay más de 30 cuevas para Los mas que desean meditar en un ambiente silencioso. Por la noche, volvemos a Lhasa y pasamos la noche.</p>
