<p>Luego volvemos a Pokhara, donde podemos hacer un paseo en bote en el lago Phewa, el lago más grande de Pokhara. También visitaremos el Templo Bindyabasini ubicado en el centro del lago antes de volar a Katmandú para pasar la noche. En Katmandú, cenamos junto con los representantes del Glaciar del Himalaya.</p>
