<p>El Palacio Norbulingka es el palacio de verano del Dalai Lama y alberga lo que se considera el jardín artificial más grande del Tíbet. A continuación, visitamos el Hospital Tradicional del Tíbet, que es el centro para el tratamiento, la formación, la investigación y la producción de la medicina tibetana. Noche en Lhasa.</p>
