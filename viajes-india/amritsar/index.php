                     <h1 class="banner-caption-heading">Viajes a Amritsar Paquetes 2023-2024</h1>
                     <p class="banner-caption-tag-line">
                       
 
<p>Hogar del espectacular Santuario Sikh, el Templo Dorado, Amritsar es una de las ciudades más famosas de la India. Conocida coloquialmente como Ambarsar , la ciudad fue fundada por el cuarto gurú sikh, Guru Ram Das en 1577 e inicialmente recibió su nombre como Ramdaspur . La ciudad tiene dos partes , la ciudad vieja y la ciudad nueva. La ciudad vieja se caracteriza por callejuelas estrechas, lugares históricos, puertas medievales y mercados afilados. Por el contrario, la nueva ciudad está repleta de espaciosos centros comerciales y lujosos hoteles que se encuentran dispersos de manera no planificada.</p>


                     <div class="more-read">
                        <p>Es muy interesante notar que la primera piedra del Santuario Sikh más hermoso, el Templo dorado, fue colocada por un santo sufí Pir Mian Mir de Lahore.</p>
<p>El Templo Dorado inicialmente no estaba hecho de oro. El rey Ranjit Singh lo reconstruyó y lo cubrió de oro después de que el gobernante de Afganistán, Ahmed Shah Abdali , lo atacara dos veces .</p>
<p>La frontera de Wagah es el único cruce de carreteras entre India y Pakistán, por lo que a menudo se alude a ella como el Muro de Berlín de Asia.</p>
<h2>Qué ver en Amritsar</h2>
<p><strong>templo de Oro</strong></p>
<p>Sri Harmandir Sahib o el Templo Dorado es un lugar religioso central no solo para los sijs sino también para personas de todas las religiones. Este santuario único es visitado por personas de todas las religiones y todos los ámbitos de la vida.</p>
<p><strong>Jallianwala Bagh</strong></p>
<p>Este es el sitio de la infame masacre de Amritsar en la que el ejército británico mató brutalmente a cientos de civiles inocentes. Todavía se pueden ver las marcas de bala en los altos muros del bagh .</p>
<p><strong>Frontera de Wagah</strong></p>
<p>Visitada por más de 5000 personas todos los días, la frontera de Wagah es el único paso fronterizo entre India y Pakistán. La mayoría de las personas visitan la frontera de Wagah para presenciar la ceremonia de "Retiro de golpes" que se lleva a cabo todas las noches antes del atardecer.</p>
<h2>Chupete de labios cocina de Amritsar</h2>
<p><strong>Kesar da dhaba</strong></p>
<p>Situado en los pequeños barrancos de Amritsar se encuentra el mejor dhaba de Amritsar. Este Dhaba vegetariano puro sirve comida cocinada en puro desi ghee y te hace babear. el dal makhni y phirni son la parte más deliciosa de la comida.</p>
<p><strong>kulcha famosa de la India wala</strong></p>
<p>Sería una pena que no visites el cruce de Chungi mientras estás en Amritsar. Debes comer el famoso Amritsari kulcha y coro coro Naan en el All India Famous Kulcha Wala en el cruce de Chungi, Magbool Road . La masa de la kulcha se amasa con mucho desi ghee puro y tiene varias capas. La kulcha simplemente se derrite en la boca.</p>
<p><strong>gurú ka langar</strong></p>
<p>Es una auténtica delicia comer el langar que se sirve en el Templo Dorado. El langar es un festín que los voluntarios preparan en la cocina del templo. El Prasad se sirve todos los días y personas de todos los ámbitos de la vida vienen a comer el Prasad en grandes cantidades. Los visitantes se sientan en filas ordenadas y los voluntarios les sirven la comida.</p>
<p><strong>Ahuja Lassi</strong></p>
<p>Cargado con rico y cremoso malai , el Ahuja lassi es algo que no habrías probado en ninguna parte del mundo. El vaso alto de lassi es suficiente para llenar el estómago y hacer que te quedes dormido.</p>
<p><strong>Cómo moverse en Amritsar</strong></p>
<p>La ciudad está muy bien comunicada por autobuses y trenes. Uno puede viajar usando autobuses urbanos, trenes y alquilando un taxi. Se pueden recorrer pequeñas distancias en ciclo-rickshaws y auto-rickshaws.</p>
<h2>Dónde comprar en Amritsar</h2>
<p><strong>Mercado del Templo Dorado</strong></p>
<p>El mercado ubicado a las afueras del Templo Dorado tiene varias tiendas que están surtidas con varios artículos de importancia religiosa para los sijs. Estos incluyen dagas, espadas, karas y phulkari. dupattas para mujer.</p>
<p><strong>Bazar Gurú</strong></p>
<p>Este es un mercado muy antiguo y es famoso por las joyas tradicionales de plata y oro. Con más de 2500 tiendas, atrae a mujeres de todas las edades. Aquí se pueden conseguir todo tipo de adornos tradicionales hechos de oro, plata y piedras preciosas y semipreciosas. En este mercado también se pueden comprar ollas de barro, diyas de plata e ídolos de dioses y diosas hindúes.</p>
<p><strong>Bazar Papad-wadian</strong></p>
<p>Padas y wadian son las patatas fritas y galletas saladas tradicionales que adoran la mayoría de los punjabíes. En este mercado, puedes comprar papads caseros frescos y wadian para comer en tu casa. Como tienen una larga vida útil, puedes almacenarlos durante meses sin preocuparte.</p>
<p><strong>Bazar Mishri</strong></p>
<p>Este bazar es famoso por los frutos secos. En este mercado se pueden comprar frutos secos afganos especiales. <br /> La ciudad de Amritsar es una ventana a la lucha por la libertad de la India. La frontera de Wagah y Jallianwala bagh trae instantáneamente una serie de imágenes de la lucha por la libertad y los sacrificios de los luchadores por la libertad de la India. Una mirada a la ciudad vieja te transporta a la época anterior a la independencia. Realmente vale la pena visitar esta ciudad. Uno puede encontrar fácilmente vuelos baratos a Amritsar.</p>
 
