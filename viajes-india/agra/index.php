                     <p class="banner-caption-tag-line">
                       
La fascinante belleza de las gemas mogoles de Agra debe verse para creerse

Agra es sinónimo del Taj Mahal y es fácil ver por qué. Este magnífico monumento no solo aparece en muchas listas de "lo mejor de"; también es una Maravilla del Mundo, junto con la Gran Muralla China y Petra. Pero los viajeros con destino a la India estarán encantados de saber que Agra ofrece más de una estructura espectacular para explorar.                     </p>

                     <div class="more-read">
                        <p>                          


<p><strong>Fuerte de Agra</strong></p>
<p>Este fuerte de arenisca fue el hogar principal de los emperadores mogoles desde 1573 hasta 1638, después de lo cual la capital se trasladó de Agra a Delhi. Su historia, sin embargo, se remonta a 1526 cuando el primer emperador mogol Babur capturó el fuerte. Cambió de manos en varias guerras, solo para terminar nuevamente en manos de los mogoles.</p>
<p>A Akbar el Grande, el más famoso de los emperadores mogoles, se le atribuye la restauración del fuerte a su esplendor actual. Se necesitaron 4000 hombres durante ocho años para completar este hermoso sitio declarado Patrimonio de la Humanidad por la UNESCO.</p>
<p>Dentro del fuerte, encontrarás muchos edificios diferentes, desde salones hasta palacios, que se dice que están conectados por pasadizos y túneles secretos. Para mantener frescas las estructuras de mármol durante los calurosos meses de verano, las paredes se dejaron huecas y se llenaron de agua corriente. Genial.</p>
<p><strong>jama mezquita</strong></p>
<p>Una vez conectada con el Fuerte Rojo, Jama Masjid es una de las mezquitas más grandes construidas por los mogoles. El emperador Shah Jahan, quien también construyó el Taj Mahal, encargó la mezquita en honor a su hija Jahan Ara Begum.</p>
<p><br />Jama Masjid cuenta con elementos de diseño de influencia persa e india. Hay cuatro torres y dos minaretes de 40 metros hechos de piedra arenisca roja y mármol blanco. El patio tiene capacidad para unos impresionantes 25.000 fieles y la sala de oración es algo de gran belleza (busque la colección de reliquias de Mahoma).</p>
<p><br />Como en la mayoría de las ciudades de la India, las opciones de comida en Agra ciertamente valen la pena. Hay una gran cantidad de excelentes restaurantes que se especializan en rica comida Mughlai: pruebe los kebabs, el pollo con mantequilla y las parathas (pan sin levadura). Los golosos pueden probar la petha (hecha de calabaza blanca y nueces), que se puede comprar en muchos puestos callejeros.</p>
<p>Después de un ajetreado día de turismo, relájate con una copa (o dos) en Amarvilas. El hotel de The Oberoi Group es tan hermoso como cabría esperar, pero es el bar que amamos. Con vistas ininterrumpidas del Taj Mahal, no hay mejor manera de despedirse de esta colorida ciudad.<br /><br /></p>

