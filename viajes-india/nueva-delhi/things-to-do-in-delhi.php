<p>India is surrounded by beaches on all three sides and there are plenty of beaches here. Beaches are hidden gems of India and they are meant for a party and exploring another side of India. Some of them are:</p>
<ul>
<li>The Palolem beach of Goa is a paradise on Earth. It is truly heaven with bays forming magnificent views and you can see fishermen here to catch some of the best kinds of seafood. Dolphins also make their appearances in unexpected means.</li>
<li>Radhanagar beach on the Andaman Islands is considered to be one of the beautiful beaches of Asia. It is a perfect place to bask in the day. The water is crystal blue and the sands are warm and soft. The lush green vegetation attracts you to this place.</li>
</ul>
<p>The best time to visit beaches is from October to March. You can encounter the Sunburn festival in Goa during December and the whole environment just changes.</p>

<h2>Backwaters of Kerala</h2>
<p>Kerala is known for its backwaters and paddy fields. Take a canoe to the backwaters where you can find mangroves and everything just so fantastic. A backwater of Kerala is spread across a distance of 900 km and staying in a boathouse in the backwaters is a dream comes true for many. The recommended backwaters are:</p>
<ul>
<li>Alleppey backwaters give you a rare sight of a village of India. It has floating houseboats and people living on the coastline. The vegetation of Alleppey is below the sea level.</li>
<li>Kollam located at the end of Kerala has one of the oldest ports of India. The backwaters are located near mountains and green fields. It has the Ashtamundi Lake just to the end of the backwaters.</li>
</ul>
<p>The vegetation can be best viewed between the months of October to April.</p>

<h2>Meditation and Yoga</h2>
<p>India is home to meditation and Yoga which relaxes one&rsquo;s mind and soul. Different communities of India have started with Yoga to rejuvenate the body. Some places worth visiting for meditation are:</p>
<ul>
<li>Varanasi is the place where Hinduism is given utmost importance. With Ghats and the sacred Ganga located in it, Varanasi is an awesome place for yoga and chants. It is the place of Lord Shiva and the location is meant for people who are in search of peace and calmness.</li>
<li>Rishikesh is the Yoga Capital of India as it has many shrines and the place is considered holy. The hills and the deserted caves are best places for yoga and meditation. Yoga classes are provided to its visitors in Rishikesh.</li>
</ul>
<p>Varanasi is the best to visit from November to March as there are so many activities going on during that time.</p>

<h2>Camel Safaris</h2>
<p>If you are in Rajasthan, trying out camel safaris is a must. Camping after a camel safari can give you new horizons in life. Experience the rural life of Rajasthan. Camel rides are not comfortable but worth the experience. Some of the places to try out safaris are:</p>
<ul>
<li>Jaisalmer, with its sand dunes and high hills of sands, trying out camel safari in this place is like a dream come true. Try out safaris at night under the stars and it will not disappoint you. The Golden City Jaisalmer has camels lined up in narrow streets.</li>
<li>The Rann of Kutch, known as the land of white desert, is the only salt plain in India. It has lunar-like landscapes with the best view of the sunset. Wolves, donkeys and desert cats are some of the species that you are going to find out.</li>
</ul>
<p>The cooler months of India are best to visit deserts. September to March is the best time to visit.</p>
<h2>Historic places to visit</h2>
<p>India is a place with many historic cultures with the downfall of many kings and kingdoms. Because of its significance, India has had a glorious past some of which are:</p>
<ul>
<li>The Taj Mahal is preserved by UNESCO and is one of the Seven Wonders of the World made of white marble. It was made by Shah Jahan in memory of his wife. It is a jewel of architecture and Muslim heritage.</li>
<li>The Red Fort made of red sandstone was made by the first Mughal Emperor in the earliest times and is located in Delhi. It is a mixture of Persian and Hind architecture.</li>
</ul>
<p>Monuments should be visited early in the morning to enjoy it nicely as it is less crowded at that moment.</p>

<h2>Street foods of India</h2>
<p>India is a land of herbs and natural spices and it is a place where a foodie should visit once in their lifetime. It has a different assortment of dishes and tastes that are unique. Some of the places for awesome food are:</p>
<ul>
<li>Karims: It is located in Old Delhi by Mughals and it is famous even today. The royal recipe of Mughals was made and that secret recipe even attracts people to Karims today.</li>
<li>Bharawan da Dhaba in Punjab was established in the year 1912. Chana, Paneer and Kofta are some of the dishes which are a must try in this place. Pilgrims from different parts of the world come to this place to savour the delectable tastes.</li>
</ul>

<h2>Shopping in India</h2>
<p>India is a shopping destination and not just malls, but street shopping is a treat for those shopaholics. The charms of old markets and industry of crafts and handicrafts are what everyone loves.</p>
<ul>
<li>Mysore is known for its silk sarees and it is a market to handicrafts products. Sandalwood is found in pure in Mysore. An artist can find their love in Mysore and can buy to their heart's content.</li>
<li>Now, if in India, you have to visit Hyderabad because of its leather goods and pearls. Indian jewellery is found at its very best. You will love shopping here totally.</li>
</ul>
<p>India is the perfect place for travellers as there are so many things to experience here. You will love it in India. Plan for a holiday soon.</p>
