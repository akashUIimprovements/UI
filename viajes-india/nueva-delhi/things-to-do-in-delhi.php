<p>You are sure to find a temple at a distance of 3 km in India. There are so many and each one is unique in its own ways. People go to temples to pray and seek blessings of Almighty. Whether the architecture or the lineage of historic artefacts, temples are an epitome of praying and wishing upon. Some of the holiest temples recognised are:</p>
<ul>
<li>Meenakshi Amman Temple in Madurai is at the heart of the city and it is really old. This temple has a thousand pillars, 14 gopurams and about 30,000 sculptures which are different from each other. It has a golden vimana and you can find many visitors to this temple every day.</li>
<li>The Golden Temple is located in Amritsar and it is in the middle of a lake and it is so peaceful and quiet. The Sikhs represent togetherhood in this temple and this temple is specifically known for its glory and heritage. Everyone is welcomed to this temple and all are treated equally.</li>
<li>The Akshardham temple, located in Delhi is dedicated to Swami Narayan and it is so majestic and beautiful that you will simply feel lost in time as you enter this temple. This temple was built by about 11,000 artisans and it took a period of 5 years to build it. It has a massive garden and an exquisite fountain. Because of its splendour and valour, this temple is known for its historic values.</li>
</ul>
<p>The best time to visit all of these temples is almost all through the year and they are open on all days for visitors. It is during the early hours of the morning that you are going to find a lot of people.</p>
<h2>Various festivals in India</h2>
<p>The festivals celebrated in India are full of fun and zeal. There are a lot many festivals celebrated a whole year. Some are for gods and goddesses while some are simply because of entry of new seasons. Some important festivals in India are:</p>
<ul>
<li>Holi, a festival that falls during the spring season is the colourful festival celebrated in India. You can see people coming to the streets to put colours on each other and wish them blessings and good wishes. Holi marks the important significance of Good over evil. It is celebrated with great pomp and show.</li>
<li>The next important festival in India is Diwali and it is the festival of lights. It is a five-day festival where people put diyas and candles in their houses and brighten up their places. Colourful rangolis are made it takes place between the months of October and November.</li>
</ul>
<p>Holi is best witnessed in Varanasi while the best place to celebrate Diwali is Delhi and the Northern places. You can find a market full of varieties of lights in Jaipur just for this festival.</p>
<h2>Trekking in the Himalayas</h2>
<p>Trekking is not easy but it is worth trying and what is more amazing than to trek in the Himalayas. From easy ones to the dangerous ones, you are sure to find all. The best ones are:</p>
<ul>
<li>Hampta Pass Trek which is one of the easiest ones which you can trek in the Himalayas. You are sure to see some breathtaking views and snow clad mountains. You will cross pastures and open valleys.</li>
<li>Markha Valley trek is another trek in the desert of Ladakh and Zanskar. You will cross the Hemis National Park in the meantime and Buddhist villages. The rocky canyons will leave you awestruck and excited to try this trek once.</li>
</ul>
<p>The best time to try out these treks is between May and September as the weather is good and the temperatures make it easy for you to trek.</p>

<h2>The serene beaches</h2>


