<p>Excursión a Jaisalmer</p>
<p>La ciudad de Jaisalmer se encuentra sobre una cresta de arenisca amarillenta. Su gloria suprema es su fuerte, también llamado Fuerte Dorado o Sonar Killa, que contiene un palacio y varios templos jainistas ornamentados. Jaisalmer también se llama la Ciudad Dorada, ya que se encuentra en el corazón del Gran Desierto Indio Thar y tiene arenas doradas por todas partes. Incluso la mayoría de las casas y templos aquí están hechos de piedra amarilla, lo que le da a toda la ciudad un tono amarillo. El origen de Jaisalmer en su forma actual se remonta a 1156 dC cuando Rawal Jassal Bhatti lo fundó. Los Bhatti Rajputs afirman ser descendientes directos del Señor Krishna. En la época medieval, Jaisalmer era una importante ciudad comercial y sus ricos comerciantes también contribuyeron en gran medida a su arte y arquitectura.</p>

