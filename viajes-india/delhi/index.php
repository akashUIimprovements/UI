<div class="more-read">  <p>Históricamente, Delhi es una de las ciudades más importantes de la India y ha servido como un centro político, comercial y cultural vital a lo largo de su larga historia. Es una de las ciudades más antiguas del mundo y se cree que estuvo habitada continuamente desde el siglo VI a.</p>
<p><br />Delhi ofrece los aspectos más vibrantes y coloridos de la cultura india. Un recorrido por Delhi lo lleva a maravillas arquitectónicas únicas, lugares religiosos, monumentos históricos, museos y galerías. Delhi ha sido una sede de poder desde la antigüedad.</p>
